$( document ).ready(function() {
    $('#hide-add-group-button').hide()
    $('#add-group-container').hide()

    $('#add-group-button').click(()=>{
        $('#add-group-button').hide();
        $('#add-group-container').show();
        $('#hide-add-group-button').show();
    })

    $('#hide-add-group-button').click(()=>{
        $('#hide-add-group-button').hide();
        $('#add-group-button').show();
        $('#add-group-container').hide()
    })
});
